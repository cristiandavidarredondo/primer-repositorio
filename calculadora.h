

/**
* \brief funcion que suma
* \primer valor ingresado valor1
* \segundo valor ingresado  valor2
* \return  retorna un entero con el resultado
*/
int suma (int valor1,int valor2);

/**
* \brief funcion que resta
* \primer valor ingresado valor1
* \segundo valor ingresado  valor2
* \return  retorna un entero con el resultado
*/
int resta (int valor1,int valor2);

/**
* \brief funcion que multiplica
* \primer valor ingresado valor1
* \segundo valor ingresado  valor2
* \return  retorna un entero con el resultado
*/
int multiplicacion (int valor1,int valor2);

/**
* \brief funcion que divide
* \primer valor ingresado valor1
* \segundo valor ingresado  valor2
* \return  retorna un valor flotante por que el resultado de la division puede se de punto flotante
*/
float division (int valor1,int valor2);
